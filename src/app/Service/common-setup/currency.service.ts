import { HttpClient, HttpHeaders } from '@angular/common/http';
import { Injectable } from '@angular/core';
import {catchError, Observable, of} from "rxjs";
import {ToastrService} from "ngx-toastr";
import {environment} from "../../../environment/environment";
import {Currency} from "../../model/common-setup/currency";
@Injectable({
  providedIn: 'root',
})
export class CurrencyService{
  constructor(private httpClient: HttpClient, private toastr: ToastrService) {}
  getCurrency(){
    return this.httpClient.get(environment.api_url +'/currency').pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {

      if(error.status === 404){
        this.toastr.warning(error.error.message !== null ? error.error.message: error.error.error);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }

  getCurrencyActive(){
    return this.httpClient.get(environment.api_url +'/currency/active').pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {
      if(error.status === 404){
        this.toastr.warning(error.error.message !== null ? error.error.message: error.error.error);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }

  postCurrency(currency: Currency){
    const headers = { 'content-type': 'application/json'};
    const body=JSON.stringify(currency);
    return this.httpClient.post(environment.api_url +'/currency', body,{'headers':headers}).pipe(catchError((error:any, caught:Observable<any>):Observable<any> =>{
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of()
    }))
  }
  putCurrency(currency: Currency, id:any){
    const headers = {'content-type': 'application/json'};
    const body = JSON.stringify(currency);
    return this.httpClient.put<any>(environment.api_url +'/currency?id='+id,body,{'headers':headers}).pipe(catchError((error:any, caught:Observable<any>):Observable<any> =>{
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }))
  }
  deleteCurrency(id:any){
    return this.httpClient.delete(
      environment.api_url +'/currency',
      {
        params: {
          id: id,
        },
      }
    ).pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }
}
