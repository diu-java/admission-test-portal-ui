import { HttpClient, HttpHeaders } from '@angular/common/http';
import { Injectable } from '@angular/core';
import {catchError, Observable, of} from "rxjs";
import {ToastrService} from "ngx-toastr";
import {environment} from "../../../environment/environment";
import {Bank} from "../../model/common-setup/bank";
@Injectable({
  providedIn: 'root',
})
export class BankService{
  constructor(private httpClient: HttpClient, private toastr: ToastrService) {}
  getBank(){
    return this.httpClient.get(environment.api_url +'/bank').pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {

      if(error.status === 404){
        this.toastr.warning(error.error.message !== null ? error.error.message: error.error.error);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }

  getBankActive(){
    return this.httpClient.get(environment.api_url +'/bank/active').pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {
      if(error.status === 404){
        this.toastr.warning(error.error.message !== null ? error.error.message: error.error.error);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }

  postBank(bank: Bank){
    const headers = { 'content-type': 'application/json'};
    const body=JSON.stringify(bank);
    return this.httpClient.post(environment.api_url +'/bank', body,{'headers':headers}).pipe(catchError((error:any, caught:Observable<any>):Observable<any> =>{
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of()
    }))
  }
  putBank(bank: Bank, id:any){
    const headers = {'content-type': 'application/json'};
    const body = JSON.stringify(bank);
    return this.httpClient.put<any>(environment.api_url +'/bank?id='+id,body,{'headers':headers}).pipe(catchError((error:any, caught:Observable<any>):Observable<any> =>{
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }))
  }
  deleteBank(id:any){
    return this.httpClient.delete(
      environment.api_url +'/bank',
      {
        params: {
          id: id,
        },
      }
    ).pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }
}
