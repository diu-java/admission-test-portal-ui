import { HttpClient, HttpHeaders } from '@angular/common/http';
import { Injectable } from '@angular/core';
import {catchError, Observable, of} from "rxjs";
import {ToastrService} from "ngx-toastr";
import {environment} from "../../../environment/environment";
import {Religion} from "../../model/common-setup/religion";
@Injectable({
  providedIn: 'root',
})
export class ReligionService{
  constructor(private httpClient: HttpClient, private toastr: ToastrService) {}
  getReligion(){
    return this.httpClient.get(environment.api_url +'/religion').pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {

      if(error.status === 404){
        this.toastr.warning(error.error.message !== null ? error.error.message: error.error.error);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }

  getReligionActive(){
    return this.httpClient.get(environment.api_url +'/religion/active').pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {
      if(error.status === 404){
        this.toastr.warning(error.error.message !== null ? error.error.message: error.error.error);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }

  postReligion(religion: Religion){
    const headers = { 'content-type': 'application/json'};
    const body=JSON.stringify(religion);
    return this.httpClient.post(environment.api_url +'/religion', body,{'headers':headers}).pipe(catchError((error:any, caught:Observable<any>):Observable<any> =>{
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of()
    }))
  }
  putReligion(religion: Religion, id:any){
    const headers = {'content-type': 'application/json'};
    const body = JSON.stringify(religion);
    return this.httpClient.put<any>(environment.api_url +'/religion?id='+id,body,{'headers':headers}).pipe(catchError((error:any, caught:Observable<any>):Observable<any> =>{
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }))
  }
  deleteReligion(id:any){
    return this.httpClient.delete(
      environment.api_url +'/religion',
      {
        params: {
          id: id,
        },
      }
    ).pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }
}
