import { HttpClient, HttpHeaders } from '@angular/common/http';
import { Injectable } from '@angular/core';
import {catchError, Observable, of} from "rxjs";
import {ToastrService} from "ngx-toastr";
import {environment} from "../../../environment/environment";
import {Gender} from "../../model/common-setup/gender";
@Injectable({
  providedIn: 'root',
})
export class GenderService{
  constructor(private httpClient: HttpClient, private toastr: ToastrService) {}
  getGender(){
    return this.httpClient.get(environment.api_url +'/gender').pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {

      if(error.status === 404){
        this.toastr.warning(error.error.message !== null ? error.error.message: error.error.error);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }

  getGenderActive(){
    return this.httpClient.get(environment.api_url +'/gender/active').pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {
      if(error.status === 404){
        this.toastr.warning(error.error.message !== null ? error.error.message: error.error.error);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }

  postGender(gender: Gender){
    const headers = { 'content-type': 'application/json'};
    const body=JSON.stringify(gender);
    return this.httpClient.post(environment.api_url +'/gender', body,{'headers':headers}).pipe(catchError((error:any, caught:Observable<any>):Observable<any> =>{
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of()
    }))
  }
  putGender(gender: Gender, id:any){
    const headers = {'content-type': 'application/json'};
    const body = JSON.stringify(gender);
    return this.httpClient.put<any>(environment.api_url +'/gender?id='+id,body,{'headers':headers}).pipe(catchError((error:any, caught:Observable<any>):Observable<any> =>{
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }))
  }
  deleteGender(id:any){
    return this.httpClient.delete(
      environment.api_url +'/gender',
      {
        params: {
          id: id,
        },
      }
    ).pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }
}
