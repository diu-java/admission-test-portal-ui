import { HttpClient, HttpHeaders } from '@angular/common/http';
import { Injectable } from '@angular/core';
import {catchError, Observable, of} from "rxjs";
import {ToastrService} from "ngx-toastr";
import {environment} from "../../../environment/environment";
import {Country} from "../../model/common-setup/country";
@Injectable({
  providedIn: 'root',
})
export class CountryService{
  constructor(private httpClient: HttpClient, private toastr: ToastrService) {}
  getCountry(){
    return this.httpClient.get(environment.api_url +'/country').pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {

      if(error.status === 404){
        this.toastr.warning(error.error.message !== null ? error.error.message: error.error.error);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }

  getCountryActive(){
    return this.httpClient.get(environment.api_url +'/country/active').pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {
      if(error.status === 404){
        this.toastr.warning(error.error.message !== null ? error.error.message: error.error.error);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }

  postCountry(country: Country){
    const headers = { 'content-type': 'application/json'};
    const body=JSON.stringify(country);
    return this.httpClient.post(environment.api_url +'/country', body,{'headers':headers}).pipe(catchError((error:any, caught:Observable<any>):Observable<any> =>{
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of()
    }))
  }
  putCountry(country: Country, id:any){
    const headers = {'content-type': 'application/json'};
    const body = JSON.stringify(country);
    return this.httpClient.put<any>(environment.api_url +'/country?id='+id,body,{'headers':headers}).pipe(catchError((error:any, caught:Observable<any>):Observable<any> =>{
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }))
  }
  deleteCountry(id:any){
    return this.httpClient.delete(
      environment.api_url +'/country',
      {
        params: {
          id: id,
        },
      }
    ).pipe(catchError((error: any, caught: Observable<any>): Observable<any> => {
      if(error.status === 406){
        this.toastr.warning(error.error.message);
      }else{
        this.toastr.error(error.error.error);
      }
      return of();
    }));
  }
}
